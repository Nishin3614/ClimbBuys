CSignboard * CSignboard::Create(D3DXVECTOR3 pos)
{
	// 変数宣言
	CSignboard * pSignboard = new CSignboard();
	// シーン管理設定
	pSignboard->ManageSetting(CScene::LAYER_3DOBJECT);
	// サイズ設定
	pSignboard->SetSize(D3DXVECTOR3(400.0f, 0.0f, 200.0f));
	// オフセット設定
	D3DXVECTOR3 ofset(0.0,0.0f, 50.0f);
	// 位置設定
	pSignboard->SetPos(ofset + pos);
	// 回転設定
	pSignboard->SetRot(D3DXVECTOR3(-1.57f, 0.0f, 0.0f));
	// テクスチャ設定
	//pSignboard->SetTexType(4);
	pSignboard->SetCol(D3DXCOLOR(1.0f, 0.0f, 0.0f, 0.5f));

	// 初期化処理
	pSignboard->Init();
	// 生成したオブジェクトを返す
	return pSignboard;
}

