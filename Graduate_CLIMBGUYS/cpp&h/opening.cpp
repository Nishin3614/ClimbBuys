// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// コンストラクタ
// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
COpening::COpening()
{
	m_state = OpeningState::BUILDING;
	m_pOpeningManager = nullptr;
}

// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// デストラクタ
// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
COpening::~COpening()
{

}

// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// 初期化
// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void COpening::Init(void)
{
	// モード初期化
	CBaseMode::Init();

	// オープニング管理クラスの生成
	m_pOpeningManager = new COpeningManager;
}

// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// 終了
// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void COpening::Uninit(void)
{
	// モード終了
	CBaseMode::Uninit();

	// オープニング管理クラスの解放
	if (m_pOpeningManager)
	{
		m_pOpeningManager->Uninit();
		delete m_pOpeningManager;
		m_pOpeningManager = nullptr;
	}
}

// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// 更新
// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void COpening::Update(void)
{
	// モード更新
	CBaseMode::Update();
	// 管理クラスの更新
	if (m_pOpeningManager)
	{
		m_pOpeningManager->Update();
	}

	// ゲーム遷移
	if (CCalculation::PressAnyButton())
	{
		// フェード状態が何も起こっていない状態なら
		if (CManager::GetFade()->GetFade() == CFade::FADE_NONE)
		{
			CManager::GetFade()->SetFade(CManager::MODE_TITLE);
		}
	}
}

// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// 描画処理
// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void COpening::Draw(void)
{
	// モード描画
	CBaseMode::Draw();
}

// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// 生成処理
// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
COpening * COpening::Create(void)
{
	// 変数宣言
	COpening * pOpening = new COpening;
	// 初期化処理
	pOpening->Init();
	// 生成したオブジェクトを返す
	return pOpening;
}