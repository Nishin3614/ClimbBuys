COpening::~COpening()
{

}

// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// 初期化
// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void COpening::Init(void)
{
	// モード初期化
	CBaseMode::Init();

	// タイトルUIの生成
	CUi::LoadCreate(CUi::UITYPE_TILTE);

	// 背景オブジェクトの生成
	CScene_X::Create(
		D3DXVECTOR3(300.0f, 0.0f, 500.0f),	// 位置
		D3DVECTOR3_ZERO,					// 回転
		CScene_X::TYPE_OBJECT_MOUNTAIN,		// モデル番号
		false);								// シャドウマッピング状態

}

// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// 終了
// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void COpening::Uninit(void)
{
	// モード終了
	CBaseMode::Uninit();
}

// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// 更新
// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void COpening::Update(void)
{
	// モード更新
	CBaseMode::Update();
	// ゲーム遷移
	if (CCalculation::PressAnyButton())
	{
		// フェード状態が何も起こっていない状態なら
		if (CManager::GetFade()->GetFade() == CFade::FADE_NONE)
		{
			CManager::GetFade()->SetFade(CManager::MODE_TITLE);
		}
	}
}

// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// 描画処理
// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void COpening::Draw(void)
{
	// モード描画
	CBaseMode::Draw();
}

// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// 生成処理
// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
COpening * COpening::Create(void)
{
	// 変数宣言
	COpening * pOpening = new COpening;
	// 初期化処理
	pOpening->Init();
	// 生成したオブジェクトを返す
	return pOpening;
}