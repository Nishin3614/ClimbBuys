class COpening : public CBaseMode
{
public:
	/* 列挙型 */
	// enumclass
	enum class OpeningState {
		ERUPTION,		// 噴火
		BUILDING,		// 必要素材の構築
		STAGING,		// 演出
		END,			// 終了 いらないかも
	};


	/* 関数 */
	// コンストラクタ
	COpening();
	// デストラクタ
	~COpening();
	// 初期化処理
	void Init(void);
	// 終了処理
	void Uninit(void);
	// 更新処理
	void Update(void);
	// 描画処理
	void Draw(void);
	// 生成処理
	static COpening * Create(void);

	OpeningState GetState() const {return m_state;};
	void		 SetState(OpeningState state) { m_state = state; };
protected:
private:
	OpeningState m_state;						// オープニングの状態
	COpeningManager *m_pOpeningManager;			// オープニング管理クラスのポインタ
};
