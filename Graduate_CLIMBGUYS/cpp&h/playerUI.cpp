#define PLAYER_UI_SIZE		(D3DXVECTOR3(45.0f, 0.0f, 30.0f))	// プレイヤーUIのサイズ
#define PLAYER_UI_ROT		(D3DXVECTOR3(-0.5f, 0.0f, 0.0f))	// プレイヤーUIの回転

// ----------------------------------------------------------------------------------------------------------------------------------------------------------------
//
// 静的変数宣言
//
// ----------------------------------------------------------------------------------------------------------------------------------------------------------------

// ----------------------------------------------------------------------------------------------------------------------------------------------------------------
