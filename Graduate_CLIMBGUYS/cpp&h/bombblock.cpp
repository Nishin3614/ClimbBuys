// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// 周りのブロックを削除する処理
// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void CBombblock::AroundDelete(void)
{
	// ブロックのループ
	for (int nCntBlock = 0; nCntBlock < CScene::GetMaxLayer(CScene::LAYER_3DBLOCK); nCntBlock++)
	{
		// 情報取得
		CBaseblock * pBlock = (CBaseblock *)CScene::GetScene(CScene::LAYER_3DBLOCK, nCntBlock);	// ブロック情報
		// フィールドブロックなら
		if (pBlock->GetType() == CBaseblock::TYPE_FIELD) continue;
		// 右にブロックが存在しているなら
		else if (this->GetGrid() + CBaseblock::GRID(1, 0, 0) == pBlock->GetGrid())
		{
			// 指定したベースブロックを削除する処理
			DeleteBlock(pBlock);
		}
		// 左にブロックが存在しているなら
		else if (this->GetGrid() + CBaseblock::GRID(-1, 0, 0) == pBlock->GetGrid())
		{
			// 指定したベースブロックを削除する処理
			DeleteBlock(pBlock);
		}

		// 奥にブロックが存在しているなら
		else if (this->GetGrid() + CBaseblock::GRID(0, 0, -1) == pBlock->GetGrid())
		{
			// 指定したベースブロックを削除する処理
			DeleteBlock(pBlock);
		}
		// 前にブロックが存在しているなら
		else if (this->GetGrid() + CBaseblock::GRID(0, 0, 1) == pBlock->GetGrid())
		{
			// 指定したベースブロックを削除する処理
			DeleteBlock(pBlock);
		}
		// 上にブロックが存在しているなら
		else if (this->GetGrid() + CBaseblock::GRID(0, 1, 0) == pBlock->GetGrid())
		{
			// 指定したベースブロックを削除する処理
			DeleteBlock(pBlock);
		}
		// 下にブロックが存在しているなら
		else if (this->GetGrid() + CBaseblock::GRID(0, -1, 0) == pBlock->GetGrid())
		{
			// 指定したベースブロックを削除する処理
			DeleteBlock(pBlock);
		}

	}
}

