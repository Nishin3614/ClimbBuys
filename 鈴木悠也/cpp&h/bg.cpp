//------------------------------------------------------------------------------
// 背景の生成処理
//------------------------------------------------------------------------------
CBg *CBg::Create()
{
	//メモリ確保
	CBg *pBg = new CBg;

	// シーン管理設定
	pBg->ManageSetting(CScene::LAYER_BG);

	// オフセットの設定
	pBg->SetOffset(OFFSET_TYPE_CENTER);

	// 位置の設定
	pBg->SetPosition(SCREEN_CENTER_POS);

	// サイズの設定
	pBg->SetSize(SCREEN_SIZE);

	// テクスチャの設定
	pBg->BindTexture(CTexture_manager::TYPE_BG);

	// 色の設定
	pBg->SetCol(D3DXCOLOR_INI);

	pBg->Init();

	//リターン
	return pBg;
}