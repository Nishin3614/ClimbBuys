// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// 全てのブロックを生成
// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void CStagingBlock::CreateAll()
{
	// ブロックの最大数分生成しポインタを保存
	for (int nCnt = 0; nCnt < MAX_STAGINGBLOCK; nCnt++)
	{
		m_pBlockPointer[nCnt] = CScene_X::Create(
			D3DVECTOR3_ZERO,
			D3DVECTOR3_ZERO,
			D3DXVECTOR3(2.0f, 2.0f, 2.0f),
			CScene_X::TYPE_BLOCK,
			false);
	}
}

